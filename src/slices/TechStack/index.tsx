export type TechStackProps = SliceComponentProps<Content.TechStackSlice>;

/**
 * Component for "TechStack" Slices.
 */
const TechStack = async ({ slice }: TechStackProps): Promise<JSX.Element> => {
  const client = createClient();
  const techStack = await Promise.all(
    slice.items.map((item) => {
      console.log(item);
      if (
        isFilled.contentRelationship(item.technologies) &&
        item.technologies.uid
      ) {
        return client.getByUID("technology", item.technologies.uid);
      }
    }),
  );
  console.log("techStack", techStack.length);
  return (
    <Bounded
      data-slice-type={slice.slice_type}
      data-slice-variation={slice.variation}
    >
      <PrismicRichText field={slice.primary.heading} components={components} />
      <div className="grid grid-cols-1 gap-4 lg:grid-cols-3">
        {techStack.map(
          (item, index) =>
            item && (
              <div
                key={index}
                className="rounded-xl border  border-slate-100 bg-slate-800"
              >
                <PrismicRichText
                  field={item.data.field}
                  components={components}
                />
                <PrismicRichText
                  field={item.data.subheading}
                  components={components}
                />
                <div className="mx-auto grid grid-cols-2 gap-4 px-4">
                  {item.data.technologies.map((technology, index) => (
                    <div
                      key={technology.name}
                      className="grid grid-cols-2 place-content-start place-items-center"
                    >
                      {technology.icon && (
                        <span className="flex h-6 w-6 justify-center">
                          {icons[technology.icon]}
                        </span>
                      )}
                      <p className="text-left text-xs font-semibold">
                        {technology.name}
                      </p>
                    </div>
                  ))}
                </div>
              </div>
            ),
        )}
      </div>
    </Bounded>
  );
};

export default TechStack;

